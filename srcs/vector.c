#include "rt.h"

inline double		vec3_scale(t_vec3 v1, t_vec3 v2)
{
	double	res;

	res = v1.x * v2.x + v1.y * v2.y + v1.z * v2.z;
	return (res);
}

inline t_vec3		vec3_new(double x, double y, double z)
{
	t_vec3	res;

	res.x = x;
	res.y = y;
	res.z = z;
	return (res);
}

inline t_vec3		vec3_add(t_vec3 v1, t_vec3 v2)
{
	t_vec3	res;

	res.x = v1.x + v2.x;
	res.y = v1.y + v2.y;
	res.z = v1.z + v2.z;
	return (res);
}

inline t_vec3		vec3_sub(t_vec3 v1, t_vec3 v2)
{
	t_vec3	res;

	res.x = v1.x - v2.x;
	res.y = v1.y - v2.y;
	res.z = v1.z - v2.z;
	return (res);
}

inline t_vec3		vec3_dot(t_vec3 v, double k)
{
	t_vec3	res;

	res.x = v.x * k;
	res.y = v.y * k;
	res.z = v.z * k;
	return (res);
}